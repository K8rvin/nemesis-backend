-- Миграция: для концовки "Побег на шаттле Цельс" оставляем обязательный
-- Запасной аккумулятор, а вторым предметом разрешаем либо Усиленный Сервопривод
-- (маршрут инженера), либо Тяжелый лом (маршрут разведчика).
UPDATE public.choices
SET conditions = '{"item_required":"Запасной аккумулятор","item_required_any":["Усиленный Сервопривод","Тяжелый лом"]}'::jsonb
WHERE id = 'ch_1_shuttle_hack'
  AND conditions = '{"item_required":["Запасной аккумулятор","Усиленный Сервопривод"]}'::jsonb;
